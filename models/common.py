def pw_conv2(inp,hidden_dim, kernel_size, stride, use_se, use_hs):
    return nn.Sequential(
        # pw
        nn.Conv2d(inp, inp, 1, 1, 0, bias=False),
        nn.BatchNorm2d(inp),
        h_swish() if use_hs else nn.ReLU(inplace=True),
        # dw
        nn.Conv2d(inp, inp, kernel_size, stride, (kernel_size - 1) // 2, groups=inp,
                  bias=False),
        nn.BatchNorm2d(inp),
        # Squeeze-and-Excite
        eca_layer(inp) if use_se else nn.Sequential(),
        h_swish() if use_hs else nn.ReLU(inplace=True),
    )


def p_liner2(inp, oup,use_se, use_hs):
    return nn.Sequential(
        # pw-linear
        nn.Conv2d(inp, oup, 1, 1, 0, bias=False),
        nn.BatchNorm2d(oup),
        eca_layer(oup) if use_se else nn.Sequential(),
        h_swish() if use_hs else nn.ReLU(inplace=True),
    )


class ParalleleBlock2(nn.Module):
    def __init__(self, inp, oup, hidden_dim, kernel_size, stride, use_se, use_hs):
        super(ParalleleBlock2, self).__init__()
        assert stride in [1, 2]

        self.identity = stride == 1 and inp == oup

        if inp == hidden_dim:
            self.conv = nn.Sequential(
                # dw
                nn.Conv2d(hidden_dim, hidden_dim, kernel_size, stride, (kernel_size - 1) // 2, groups=hidden_dim,
                          bias=False),
                nn.BatchNorm2d(hidden_dim),
                h_swish() if use_hs else nn.ReLU(inplace=True),
                # Squeeze-and-Excite
                eca_layer(hidden_dim) if use_se else nn.Sequential(),
                # pw-linear
                nn.Conv2d(hidden_dim, oup, 1, 1, 0, bias=False),
                nn.BatchNorm2d(oup),
            )
        else:
            self.convpw = nn.Sequential(
                pw_conv2(inp,hidden_dim, kernel_size, stride, use_se, use_hs),
                # dw_conv(inp, kernel_size, stride),
            )
            self.convpl = nn.Sequential(
                p_liner2(inp, oup,use_se, use_hs),
            )
            self.conv = nn.Sequential(
                # pw
                nn.Conv2d(inp, hidden_dim, 1, 1, 0, bias=False),
                nn.BatchNorm2d(hidden_dim),
                h_swish() if use_hs else nn.ReLU(inplace=True),
                # dw
                nn.Conv2d(hidden_dim, hidden_dim, kernel_size, stride, (kernel_size - 1) // 2, groups=hidden_dim,
                          bias=False),
                nn.BatchNorm2d(hidden_dim),
                # Squeeze-and-Excite
                eca_layer(hidden_dim) if use_se else nn.Sequential(),
                h_swish() if use_hs else nn.ReLU(inplace=True),
                # pw-linear
                nn.Conv2d(hidden_dim, oup, 1, 1, 0, bias=False),
                nn.BatchNorm2d(oup),
            )

    def forward(self, x):
        y = self.conv(x)
        pw = self.convpw(x)
        # z2=self.conv2(x)
        pl = self.convpl(x)
        if self.identity:
            return pw + pl
        else:
            return y



